      <br />
      <h3 id="Méta">Méta:</h3>
      Ses œuvres ne peuvent rester statiques, prisonnières de l'artiste lui-même… Elles doivent
      occuper l'espace, se déployer, respirer, vivre, se transformer parce que tout se transforme.
      Apparaissent des volumes, des matrices, des éléments dans l'espace, jusqu'à ce que l'espace
      fasse partie de l'œuvre elle-même et nous avec. La volonté est de dépasser sa propre pensée,
      son propre langage, pour, à la manière d'un philosophe, synthétiser à travers son art la
      pensée universelle, l'absolu, l'univers… L'œuvre devient méta. Pour lui, il est manifestement
      impossible de présenter ce qui l'entoure à travers une seule œuvre, une œuvre monobloc. Il
      produit une modélisation plus théorique qui se compose d'une œuvre répartie en plusieurs
      éléments distincts les une par rapport aux autres, démontrant le TOUT et son contraire. Cette
      modélisation aboutit à ce qu'on pourrait appeler une représentation du Grand TOUT.
