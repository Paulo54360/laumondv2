      l'intériorité. Comme l'écrit Edgard Morin, « Le vrai problème c'est que nous avons trop bien
      appris à séparer. Il vaut mieux apprendre à relier. Relier, c'est-à-dire pas seulement établir
      bout à bout une connexion, mais établir une connexion qui se fasse en boucle. » La recherche
      de Laumond est celle d'une reconstruction de lien, de boucle, de cercle dans lequel le
      mouvement de la vie peut s'écrire dans son infinie diversité et ses infinis possibles.
      Recherche d'un modèle universel : Sa quête est celle d'une écriture d'un modèle universel. «
