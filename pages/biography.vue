      l'éclatement, les équilibres dans le déséquilibre, les paradoxes, les possibles. La vision
      qu'il en a est empathique et englobe un regard philosophique, scientifique, humaniste et
      sensible. Il a su développer un univers d'une cohérence absolue. Au service d'une lecture sans
      fin et pourtant propre à chacun. Le spectateur devient véritable acteur dans l'interprétation
      de ces paraboles. Il exprime un regard vif, profond et acéré sur le monde, sur ses formats et
      sur la "comédie humaine". Aucun repentir, aucune correction, aucun rajout ne lui sont permis
      par le choix exigeant du blanc, immaculé, parfait, lumineux, intransigeant.
