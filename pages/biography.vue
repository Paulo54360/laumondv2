      passées à transformer le carton en cuir, à réaliser des mélanges, du lait, du brou de noix, à
      étudier les phénomènes chimiques d'attraction, de répulsion, de densité... Il expérimente le
      grès, la faïence, la porcelaine, le raku, les émaux… pétrit, malaxe, déforme, cuit, martèle,
      colle. Le travail de la matière devient obsession. 15 années durant, il va travailler sur la
      réalisation d'une tâche. Explorer la peinture, ses pigments, ses composants, les phénomènes de
      solidification, d'hétérogénéité, jusqu'à contrôler, maîtriser, modéliser sous sa main
      l'expansion, les coulures de cette tâche…jusqu'à la réalisation parfaite et unique, qui
      deviendra sa signature, sa technique : l'œuvre tâchée.
      <br />
      <br />
      <h3 id="Language">Language:</h3>
      Laumond ne s'enferme pas dans sa tâche, il crée alors l'œuvre blanche, d'abord comme élément
      de mise en exergue de l'œuvre tâchée qui explose ainsi plus intensément sous nos yeux. Puis
      peu à peu l'œuvre blanche devient contrepoids, complémentaire, essentielle, et finalement
      indissociable de l'œuvre tâchée. Commence alors une période de forte intensité pour l'artiste,
      période aussi de légèreté, où il va s'amuser, jouer avec ces deux éléments. L'œuvre blanche et
      l'œuvre tâchée correspondent, se parlent, deviennent un langage, un mouvement, l'expression de
      l'artiste. De cette période prolixe, Laumond crée des milliers d'œuvres.
