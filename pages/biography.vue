      travers une seule œuvre, une œuvre monobloc. Cela m'a amené à produire une modélisation plus
      théorique qui doit se composer d'une œuvre répartie en plusieurs éléments, distincts les uns
      par rapport aux autres démontrant le TOUT et son contraire. Cette modélisation aboutit à ce
      qu'on pourrait appeler une représentation du Grand TOUT » Laumond La conception de ce qu'il
      nomme " l'Œuvre ", évoquant ainsi la globalité de son langage et la résultante de son long
      travail de recherche, est issue d'une allégorie du Big Bang. Ce moment mystérieux où le monde
      s'est autoproduit. En créant un langage propre, Laumond crée un espace multidimensionnel et
      met en perspective le paradoxe ; l'idée du signifiant et de l'insignifiant, des équilibres
      dans le déséquilibre, du plein et du vide, le passage d'un état à un autre et tout ceci dans
      une représentation d'un modèle cohérent. Cette représentation prend également en compte les
      courants artistiques, en les fusionnant, le cubisme, minimalisme, readymade, pointillisme,
      figuratif, l'abstraction, se retrouvent dans son évocation formelle.
