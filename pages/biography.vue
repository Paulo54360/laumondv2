      <h3 id="Expérimentation">Expérimentation:</h3>
      A 17 ans Laumond fabrique des scènes de vie quotidienne à l'échelle réduite... Et déjà une
      précision du geste, l'envie de ne rien laisser au hasard, tout reproduire à l'échelle ;
      pointu, exigeant, rigoureux. Très vite, c'est l'exploration de la matière sous toutes ses
