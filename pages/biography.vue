      <h3 id="Une_œuvre_holographique">Une œuvre holographique:</h3>
      Chaque volume est au service de l'ensemble et l'on peut dire que l'ensemble d'une installation
      est une œuvre en soi. Quand on parle d'hologramme, on signifie que " le tout est plus que la
      somme des parties, la partie est dans le tout, mais le tout est dans la partie ". L'exemple le
      plus courant est celui du patrimoine génétique d'un individu que l'on retrouve dans chaque
      cellule qui le compose. C'est la conscience de ce qui rassemble l'infiniment petit à
      l'infiniment grand. Il nous offre des interrogations visuelles et sensorielles sur tout ce qui
      nous entoure, le monde de l'art, ses références et la fin de la peinture, les artistes et
      mouvements qui l'ont précédé, Duchamp, Fontana, mais en analyste subtil et fécond, il apporte
      sa réponse esthétique et formelle, sa vision, son jeu kaléidoscopique et une esthétique de la
      perfection. En 2009, il conceptualise le manifeste du MétaHisme sous la forme d'une œuvre
      d'art. Dix années plus tard, il écrit une déclaration non manifestée. En 2021, il est invité
      par le Centre Culturel Européen d'Italie (ECC Italie) à exposer à l'occasion de la Biennale de
      Venise au palais Bembo. Il expose "Concordance Universelle". Il Rencontre par la même occasion
      Marion Zilio, théoricienne de l'art, critique d'art et membre de l'AICA France. Elle écrit une
      analyse qui accompagne son œuvre lors de son exposition. Il est invité à exposer dans sa ville
      d'enfance de Saint-Viance, une œuvre immersive à la réalité augmentée " Le mobile d'ouverture
      des univers parallèles " dans un espace consacré bâtiment de France.
