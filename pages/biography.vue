      <h3 id="Création_d'un_langage,_un_pari">Création d'un langage, un pari:</h3>
      Comment raconter le monde en une seule œuvre ? C'est là son challenge. Quel langage créer pour
      répondre à ce désir de cohérence et de représentation du Tout ? « L'œuvre tachée est une
      représentation originelle du TOUT, en passant par le vivant, les idées, ce qui est visible.
      L'œuvre blanche est une représentation potentielle du tout possible comme l'infini,
      l'éternité, la limite dans l'illimité, elle est toujours en opposition, reliée à l'œuvre
      tachée. Elles se positionnent toujours en dialogue l'une par rapport à l'autre englobant une
      œuvre centrale qui est une représentation différente dans la forme mais identique dans l'idée
      de la pensée... ». Laumond Là où l'écrivain utilisera les mots pour écrire son histoire, là où
      les idéogrammes ont permis avec des traits simples la création d'un langage sans limite,
      Laumond a créé un " langage universel ". Dans l'univers Laumond, sujets, pensées, concepts
      sont symbolisés par des éléments simples représentés par une " structure volumique ", forme
      variable et pourtant constante. Ses mots sont des volumes blancs ou en verre et des volumes
      travaillés en monochrome (tachés). Les volumes : Que ceux-ci soient pleins ou vides, blancs ou
      transparents, immaculés ou identifiés par leur " tache ", qu'ils soient seuls ou reliés aux
      autres, quelle que soit leur taille, leur forme, chacun de ces volumes porte son sens. Ils
      deviennent paraboles. Le lien : Reliés par une infinité de lignes grises, qui scandent,
      unifient, découpent ou relient l'espace et les œuvres entre elles, les volumes racontent
      l'histoire globale. La tache : A l'origine de son processus de création, elle est un
      monochrome travaillé à l'extrême, " malmené " à en devenir vivant. Elle aurait pu suffire,
      elle est le Big Bang, le point d'éclosion, la vision du réel, d'une identité visible de tous.
